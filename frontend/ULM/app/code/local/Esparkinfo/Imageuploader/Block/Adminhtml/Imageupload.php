class Esparkinfo_Imageuploader_Block_Adminhtml_Imageupload extends Mage_Adminhtml_Block_Widget_Grid_Container
{
    /**
     * constructor
     *
     * @access public
     * @return void
     * @author Ultimate Module Creator
